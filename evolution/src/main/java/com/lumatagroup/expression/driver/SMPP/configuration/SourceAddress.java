package com.lumatagroup.expression.driver.SMPP.configuration;

public class SourceAddress {
	private String sourceAddr;
	private String sourceAddrTon;
	private String sourceAddrNpi;
	
	public String getSourceAddr() {
		return sourceAddr;
	}
	public void setSourceAddr(final String sourceAddr) {
		this.sourceAddr = sourceAddr;
	}
	public String getSourceAddrTon() {
		return sourceAddrTon;
	}
	public void setSourceAddrTon(final String sourceAddrTon) {
		this.sourceAddrTon = sourceAddrTon;
	}
	public String getSourceAddrNpi() {
		return sourceAddrNpi;
	}
	public void setSourceAddrNpi(final String sourceAddrNpi) {
		this.sourceAddrNpi = sourceAddrNpi;
	}
}
